/* ------- Var VS Let VS Const ------- */

/* ------- Var ------- */
// It is stored in global context.
/* ------- Let ------- */
// It is not stored in global context.
/* ------- Const ------- */
// It is not stored in global context.

/* ------- Var ------- */
// It is function scope.
/* ------- Let ------- */
// It is function scope.
/* ------- Const ------- */
// It is function scope.

/* ------- Var ------- */
// It is not block scope.
/* ------- Let ------- */
// It is block scope.
/* ------- Const ------- */
// It is block scope.

/* ------- Var ------- */
// It is not re-declared but also re-assignable.
/* ------- Let ------- */
// It is not re-declared and re-assignable.
/* ------- Const ------- */
// It is not re-declared and re-assignable.

/* ------- Var ------- */
// It is hoisted.
/* ------- Let ------- */
// It is not hoisted.
/* ------- Const ------- */
// It is not hoisted.
